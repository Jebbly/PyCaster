    def split_pixel_columns(self, texture):
        # Iterate through each pixel column and add to list
        column_list = []
        for column_index in range(texture.width):
            # Take column of pixels and convert it to data
            pixel_column = texture.crop((column_index, 0, column_index + 1, texture.height))

            # Add data to list
            column_list.append(pixel_column)

        # Return the list
        return column_list

    def split_pixel_grid(self, texture):
        # Load pixel data from texture
        texture_data = texture.load()

        # Iterate through each column and row and add pixel to list
        pixel_grid = []
        for column in range(texture.width):
            column_list = []
            for row in range(texture.height):
                pixel_color = texture_data[column, row]
                column_list.append(pixel_color)
            pixel_grid.append(column_list)

        # Return grid data
        return pixel_grid
